class ArithmeticDemo2 {

     public static void main (String[] args){
          int a = 9;
          int b = 13;
		  
          int result = a + b; // result is now 22
          System.out.println(result);

          result = a - b; // result is now -4
          System.out.println(result);

          result = a * b; // result is now 117
          System.out.println(result);

          result = a / b; // result is now 0
          System.out.println(result);

          result = a % b; // result is now 9
          System.out.println(result);

     }
}
